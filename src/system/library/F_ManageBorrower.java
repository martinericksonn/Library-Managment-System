    private void tableBBMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableBBMouseClicked

        JTable source = (JTable) evt.getSource();
        int row = source.rowAtPoint(evt.getPoint());
        String refID = source.getModel().getValueAt(row, 0) + "";
        String status = source.getModel().getValueAt(row, 3) + "";
        bookID = source.getModel().getValueAt(row, 1) + "";
        long diffDays = 0;
        Duration dif = null;
        DateFormat formatDate = new SimpleDateFormat("MMMMM dd, yyyy");
        
        try {
            String text = "SELECT refID,dateStart,dateEnd,isLate,returnDate,fee \n"
                    + "  FROM borrowedBooks where refID = ?;";
            preparedStatement = conn.prepareStatement(text);
            preparedStatement.setString(1, refID);
            resultSet = preparedStatement.executeQuery();

            if (resultSet.next()) {

                refNum.setText(resultSet.getString("refID"));
                borrowDate.setText(formatDate.format(resultSet.getDate("dateStart")));
                dueDate.setText(formatDate.format(resultSet.getDate("dateEnd")));

                if (status.equalsIgnoreCase("Not Returned")) {
                    returnBtn.setEnabled(Boolean.TRUE);
                    labelStatus.setText("Date Today                 :");

                    LocalDate d1 = LocalDate.parse(resultSet.getString("dateStart"), DateTimeFormatter.ISO_LOCAL_DATE);
                    LocalDate d2 = LocalDate.now(ZoneId.of("Asia/Tokyo"));
                    dif = Duration.between(d1.atStartOfDay(), d2.atStartOfDay());
                    diffDays = dif.toDays();

                    if ((diffDays -= 7) > 0) {
                        isLate.setText(diffDays + " day/s late");
                        isLate.setForeground(Color.red);
                        fee = diffDays * 10;
                    } else {
                        isLate.setText("Not late");
                        isLate.setForeground(Color.black);
                        fee = 0;
                    }
                    lateFee.setText(Float.toString(fee));
                    todayDate.setText(formatDate.format(Date.valueOf(d2.toString())));

                } else if (status.equalsIgnoreCase("Returned")) {
                    LocalDate d1 = LocalDate.parse(resultSet.getString("dateStart"), DateTimeFormatter.ISO_LOCAL_DATE);
                    LocalDate d2 = LocalDate.parse(resultSet.getString("returnDate"), DateTimeFormatter.ISO_LOCAL_DATE);
                    dif = Duration.between(d1.atStartOfDay(), d2.atStartOfDay());

                    lateFee.setText(resultSet.getString("fee"));
                    labelStatus.setText("Date Returned             :");
                    todayDate.setText(formatDate.format(resultSet.getDate("returnDate")));
                   
                    returnBtn.setEnabled(Boolean.FALSE);
                    diffDays = dif.toDays();
                    if ((diffDays -= 7) > 0) {
                        
                        isLate.setText(diffDays + " day/s late");
                       isLate.setForeground(Color.red);
                    } else {
                        isLate.setText("Not late");
                         isLate.setForeground(Color.getHSBColor(0, 0, 0));
                    }
                }
            }
            preparedStatement.close();
            resultSet.close();
        } catch (SQLException ex) {
            Logger.getLogger(F_ManageBorrower.class.getName()).log(Level.SEVERE, null, ex);
        }

        // JOptionPane.showMessageDialog(null, s);
    }//GEN-LAST:event_tableBBMouseClicked
 
  
