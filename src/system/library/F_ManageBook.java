    private void searchBookIDKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchBookIDKeyReleased
        try{
            String text="SELECT bookID \"Book ID\", title \"Book Title\", publisher \"Publisher\",\n" +
            " subject \"Subject\" , stocks  \"Stocks\" ,description\n" +
            " FROM Books WHERE bookID = ?;";
            preparedStatement =conn.prepareStatement(text);
            preparedStatement.setString(1, searchBookID.getText());
            resultSet = preparedStatement.executeQuery();

            if(resultSet.next()){
                booktitle.setText(resultSet.getString("Book Title"));
                subject.setText(resultSet.getString("Subject"));
                publ.setText(resultSet.getString("Publisher"));
                quant.setText(resultSet.getString("Stocks"));

                preparedStatement.close();
                resultSet.close();
            }else {
                booktitle.setText("");
                subject.setText("");
                publ.setText("");
                quant.setText("");
            }
        }
        catch(Exception ex){ex.printStackTrace();}
    }//GEN-LAST:event_searchBookIDKeyReleased

    private void AddQuantaddQuantActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddQuantaddQuantActionPerformed
        if(!booktitle.getText().equalsIgnoreCase("")){
            error2.setText("");
            try {
                String text = "UPDATE books SET stocks = stocks + ? WHERE bookID = ?";
                preparedStatement = conn.prepareStatement(text);
                preparedStatement.setString(1, quanSelect.getItemAt(quanSelect.getSelectedIndex()));
                preparedStatement.setInt(2, Integer.parseInt(searchBookID.getText()));
                preparedStatement.executeUpdate();
                preparedStatement.close();
                JOptionPane.showMessageDialog(null, "Sucessfully added to database!");
            } catch (SQLException ex) {
                Logger.getLogger(F_ManageBook.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else
        error2.setText("book not found!");
    }//GEN-LAST:event_AddQuantaddQuantActionPerformed

