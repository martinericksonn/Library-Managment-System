    private void saveAddBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveAddBookActionPerformed
        if(!(AddQuant.getText().matches("[A-Za-z]{1}") || addBookCopyr.getText().matches("[A-Za-z]{1}")))
        if(!(addBookTitle.getText().equalsIgnoreCase("")
            || addBookQuan.getText().equalsIgnoreCase("")
            || addBookSub.getText().equalsIgnoreCase("")
            || addBookCopyr.getText().equalsIgnoreCase("")))

    try{
        String text="INSERT INTO books VALUES(?,?,?,?,?,?,?,?,?)";
        preparedStatement =conn.prepareStatement(text);
        preparedStatement.setInt(1, Integer.parseInt( bookID.getText()));
        preparedStatement.setString(2, addBookTitle.getText());

        preparedStatement.setInt(3,Integer.parseInt(addBookCopyr.getText()));
        preparedStatement.setString(4, addBookCon.getText());
        preparedStatement.setString(5, addBookPub.getText());
        preparedStatement.setString(6, addBookDesc.getText());
        preparedStatement.setString(7, addBookISBN.getText());
        preparedStatement.setString(8, addBookSub.getText());
        preparedStatement.setInt(9,Integer.parseInt(addBookQuan.getText()));

        preparedStatement.executeUpdate();

        JOptionPane.showMessageDialog(null, "Sucessfully added to database!");
        //setnull kung null
        preparedStatement.close();
        }
        catch(Exception ex){ex.printStackTrace();}
        else error.setText("please fill out all required fields");
        else error.setText("invalid input format");
    }//GEN-LAST:event_saveAddBookActionPerformed

