    private void onClick(){
           String text = null;
        if(jComboBox1.getItemAt(jComboBox1.getSelectedIndex()).equalsIgnoreCase("Student List") ){
             text="  SELECT s.studID \"Student ID\", lastName \"Last Name\", firstName \"First Name\", gender \"Gender\", yrLevel \"Year Level\" ,\n" +
            "  IF (accountID IS NULL,'No','Yes') AS \"Library Account\"\n" +
            "  FROM student s\n" +
            "  left JOIN studentsAccounts sa on sa.studID = s.studID ;" ; 
        }else if(jComboBox1.getItemAt(jComboBox1.getSelectedIndex()).equalsIgnoreCase("Student Without Library Account") ){
                text = "   SELECT  s.studID \"Student ID\", lastName \"Last Name\", firstName \"First Name\", \n" +
            "  gender \"Gender\", yrLevel \"Year Level\" , IF (accountID IS NULL,'No','Yes') AS \"Library Account\"\n" +
            "  FROM student s LEFT JOIN studentsaccounts sa on sa.studID = s.studID\n" +
            "  where accountID is null;";
        } else if(jComboBox1.getItemAt(jComboBox1.getSelectedIndex()).equalsIgnoreCase("Student With Library Account") ){
            text="   SELECT s.studID \"Student ID\", lastName \"Last Name\", firstName \"First Name\", gender \"Gender\", yrLevel \"Year Level\" ,\n" +
            "  IF (accountID IS NULL,'No','Yes') AS \"Library Account\"\n" +
            "  FROM student s\n" +
            "  INNER JOIN studentsAccounts sa on sa.studID = s.studID ;" ;  
       }
        try{
         
          preparedStatement =conn.prepareStatement(text);
        //  preparedStatement.setString(1, "%"+ searchBookSubject.getText() + "%");
          resultSet = preparedStatement.executeQuery();
          jTable1.setModel(DbUtils.resultSetToTableModel(resultSet));

         preparedStatement.close();
         resultSet.close();
          }
        catch(Exception ex){ex.printStackTrace();} 
    }
