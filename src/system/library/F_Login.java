    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed
        // conn = (Connection) new ConnectSQL();
        ConnectSQL test =  new ConnectSQL();
        conn = test.ConnecrDb();
//                    setVisible(false);
//                    new F_Home().setVisible(Boolean.TRUE);
        if(username.getText().equalsIgnoreCase("Username") || username.getText().equalsIgnoreCase("") || pass.getText().equalsIgnoreCase(""))
        JOptionPane.showMessageDialog(this,"Username or Password is Empty!");
        else{
            String sql="SELECT studID,pass FROM studentsAccounts where studID=? and pass=?";
            try{
                pst= (PreparedStatement) conn.prepareStatement(sql);
                pst.setString(1,username.getText());
                pst.setString(2,pass.getText());
                rs=pst.executeQuery();
                //System.out.println("adsf");
                if(rs.next()){
                    rs.close();
                    pst.close();
                  //  JOptionPane.showMessageDialog(null, "Nays! sakto imong Username og Password");
                    setVisible(false);
                    F_Home ob = new F_Home();
                    ob.setVisible(Boolean.TRUE);
                } else
                JOptionPane.showMessageDialog(null, "Incorrect Username and Password");

            }catch(Exception e){
            } finally{
                try{
                    rs.close();
                    pst.close();
                }catch(Exception ex){
                }
            }
        }
    }//GEN-LAST:event_LoginActionPerformed

